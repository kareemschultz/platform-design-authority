def find_biome_binary() -> Path | None:
    name = "biome.exe" if sys.platform == "win32" else "biome"
    candidate = ROOT / "node_modules" / ".bin" / name
    return candidate if candidate.exists() else None


def biome_format(target_dir: Path, content: str) -> str:
    """Format generated TypeScript with the repository's own Biome config.

    `bunx ultracite check packages/contracts` lints these generated files like
    any other TypeScript source (root `check` only excludes `apps`/`packages`
    from *outside* those directories, not generated files within them), so the
    committed output must already match what Biome's formatter produces.
    Rather than hand-reimplementing Biome's line-wrapping rules in Python, the
    repository's own pinned `biome` binary formats a scratch copy of the file
    (kept inside `target_dir` so Biome's upward config resolution still finds
    the root `biome.json`) and the formatted bytes are read back.
    """
    binary = find_biome_binary()
    if binary is None:
        print(
            "warning: node_modules/.bin/biome not found; run `bun install` first. "
            "Emitting unformatted TypeScript.",
            file=sys.stderr,
        )
        return content
    fd, tmp_name = tempfile.mkstemp(dir=target_dir, prefix=".generate_contracts_fmt_", suffix=".ts")
    tmp_path = Path(tmp_name)
    try:
        with open(fd, "w", encoding="utf-8", newline="\n") as handle:
            handle.write(content)
        result = subprocess.run(
            [str(binary), "format", "--write", str(tmp_path)],
            cwd=ROOT,
            capture_output=True,
            text=True,
            check=False,
        )
        if result.returncode != 0:
            raise ValueError(
                f"biome format failed for scratch copy of a generated contract file: "
                f"{result.stdout}\n{result.stderr}"
            )
        return tmp_path.read_text(encoding="utf-8")
    finally:
        tmp_path.unlink(missing_ok=True)


