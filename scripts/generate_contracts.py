# Driver
# ---------------------------------------------------------------------------


def write_or_check(path: Path, content: str, check: bool) -> bool:
    if check and not path.parent.is_dir():
        print(f"stale contract: {path.relative_to(ROOT).as_posix()} (package directory missing)", file=sys.stderr)
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    formatted = biome_format(path.parent, content)
    if check:
        current = path.read_text(encoding="utf-8") if path.exists() else ""
        if current != formatted:
            print(f"stale contract: {path.relative_to(ROOT).as_posix()}", file=sys.stderr)
            return False
        return True
    path.write_text(formatted, encoding="utf-8", newline="\n")
    print(f"wrote {path.relative_to(ROOT).as_posix()}")
    return True


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()

    try:
        capability_ids = load_capability_ids()
        permission_ids = load_permission_ids()
        permissions_by_namespace = load_permissions_by_namespace()
        event_names, event_owners = load_events()
        endpoint_rows, manifest_count, openapi_count = build_endpoints()
    except (OSError, ValueError, KeyError, json.JSONDecodeError) as exc:
        print(f"contract generation failed: {exc}", file=sys.stderr)
        return 1

    print(
        f"joined {len(endpoint_rows)} endpoints "
        f"(manifest {manifest_count} / openapi {openapi_count}); "
        f"{len(capability_ids)} capabilities; {len(permission_ids)} permissions; "
        f"{len(event_names)} events",
        file=sys.stderr,
    )

    outputs = {
        ROOT / "packages" / "contracts" / "capabilities" / "src" / "index.ts": render_capabilities_index(
            capability_ids
        ),
        ROOT / "packages" / "contracts" / "permissions" / "src" / "index.ts": render_permissions_index(
            permission_ids, permissions_by_namespace
        ),
        ROOT / "packages" / "contracts" / "events" / "src" / "index.ts": render_events_index(
            event_names, event_owners
        ),
        ROOT / "packages" / "contracts" / "api" / "src" / "index.ts": render_api_index(
            endpoint_rows, permission_ids
        ),
        ROOT / "packages" / "contracts" / "platform-api" / "src" / "generated.ts": render_platform_api_metadata(
            endpoint_rows
        ),
    }

    ok = True
    for path, content in outputs.items():
        ok &= write_or_check(path, content, args.check)
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
