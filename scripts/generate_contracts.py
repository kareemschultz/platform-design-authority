def load_events() -> tuple[list[str], dict[str, str]]:
    data = load_json(ROOT / "registry" / "events.json")
    names = sorted(str(item["name"]) for item in data["events"])
    owners = {str(item["name"]): str(item["owner"]) for item in data["events"]}
    return names, owners


def render_events_index(event_names: list[str], owners: dict[str, str]) -> str:
    lines: list[str] = [
        GENERATED_HEADER,
        "// Source: registry/events.json",
        "",
        "export const EVENT_NAMES = [",
    ]
    for name in event_names:
        lines.append(f"\t{ts_string(name)},")
    lines.append("] as const;")
    lines.append("")
    lines.append("export type EventName = (typeof EVENT_NAMES)[number];")
    lines.append("")
    lines.append("export const EVENT_OWNERS: Record<EventName, string> = {")
    for name in event_names:
        lines.append(f"\t{ts_string(name)}: {ts_string(owners[name])},")
    lines.append("};")
    lines.append("")
    lines.append('export type { EventEnvelope } from "./envelope";')
    lines.append("")
    return "\n".join(lines) + "\n"


