def normalize_endpoint_path(route: str) -> str:
    """Normalize base-version and placeholder names for contract parity checks.

    Kept identical to scripts/validate_docs.py::normalize_endpoint_path so the
    generator's join agrees with the governance validator's join.
    """
    if route.startswith("/v1/"):
        route = route[3:]
    return re.sub(r"\{[^{}]+\}", "{}", route)


def collect_openapi_operations(path: Path) -> dict[tuple[str, str], dict[str, str]]:
    """Extract the controlled OpenAPI path surface without adding a YAML dependency.

    Adapted from scripts/validate_docs.py::collect_openapi_operations. Keys are
    (METHOD, normalized-path); values carry the operationId/permission/authorization
    plus the original (non-normalized) OpenAPI path for diagnostics.
    """
    operations: dict[tuple[str, str], dict[str, str]] = {}
    current_path: str | None = None
    current_key: tuple[str, str] | None = None
    current_section: str | None = None
    current_response_status: str | None = None
    for line_number, line in enumerate(path.read_text(encoding="utf-8").splitlines(), start=1):
        path_match = OPENAPI_PATH.match(line)
        if path_match:
            current_path = path_match.group(1)
            current_key = None
            current_section = None
            current_response_status = None
            continue
        method_match = OPENAPI_METHOD.match(line)
        if method_match and current_path:
            current_key = (method_match.group(1).upper(), normalize_endpoint_path(current_path))
            if current_key in operations:
                raise ValueError(
                    f"{path.relative_to(ROOT)}:{line_number}: duplicate OpenAPI operation "
                    f"{current_key[0]} {current_path}"
                )
            operations[current_key] = {"source_line": str(line_number), "openapi_path": current_path}
            current_section = None
            current_response_status = None
            continue
        if current_key is None:
            continue
        if line == "      requestBody:":
            current_section = "request"
            current_response_status = None
            continue
        if line == "      responses:":
            current_section = "responses"
            current_response_status = None
            continue
        if line.startswith("      ") and not line.startswith("        "):
            current_section = None
            current_response_status = None
        for field, pattern in (
            ("operation_id", OPENAPI_OPERATION_ID),
            ("permission", OPENAPI_PERMISSION),
            ("authorization", OPENAPI_AUTHORIZATION),
        ):
            match = pattern.match(line)
            if match:
                operations[current_key][field] = match.group(1)
                break
        if current_section == "responses":
            status_match = OPENAPI_RESPONSE_STATUS.match(line)
            if status_match:
                current_response_status = status_match.group(1)
                if "success_status" not in operations[current_key]:
                    operations[current_key]["success_status"] = current_response_status
                continue
            if OPENAPI_ANY_RESPONSE.match(line):
                current_response_status = None
                continue
        ref_match = OPENAPI_REF.search(line)
        if not ref_match:
            continue
        if current_section == "request" and "request_ref" not in operations[current_key]:
            operations[current_key]["request_ref"] = ref_match.group(1)
        elif (
            current_section == "responses"
            and current_response_status == operations[current_key].get("success_status")
            and "response_ref" not in operations[current_key]
        ):
            operations[current_key]["response_ref"] = ref_match.group(1)
    return operations


def build_endpoints() -> tuple[list[dict[str, Any]], int, int]:
    manifest = load_json(ROOT / "registry" / "endpoint-permissions.json").get("endpoints", [])
    operations = collect_openapi_operations(ROOT / "openapi" / "first-slice-v1.yaml")

    manifest_keys: set[tuple[str, str]] = set()
    rows: list[dict[str, Any]] = []
    for item in manifest:
        method = str(item["method"]).upper()
        route = str(item["path"])
        key = (method, normalize_endpoint_path(route))
        if key in manifest_keys:
            raise ValueError(f"registry/endpoint-permissions.json: duplicate endpoint {method} {route}")
        manifest_keys.add(key)
        operation = operations.get(key)
        if operation is None or not operation.get("operation_id"):
            raise ValueError(
                f"registry/endpoint-permissions.json: no OpenAPI operationId found for {method} {route}"
            )
        row: dict[str, Any] = {
            "method": method,
            "path": route,
            "operationId": operation["operation_id"],
            "successStatus": int(operation.get("success_status", "200")),
        }
        if operation.get("request_ref"):
            row["requestRef"] = operation["request_ref"]
        if operation.get("response_ref"):
            row["responseRef"] = operation["response_ref"]
        if item.get("permission"):
            row["permission"] = str(item["permission"])
        if item.get("authorization"):
            row["authorization"] = str(item["authorization"])
        rows.append(row)

    openapi_keys = set(operations)
    missing_from_openapi = sorted(f"{m} {p}" for m, p in (manifest_keys - openapi_keys))
    missing_from_manifest = sorted(f"{m} {p}" for m, p in (openapi_keys - manifest_keys))
    if missing_from_openapi or missing_from_manifest:
        raise ValueError(
            "endpoint parity mismatch between registry/endpoint-permissions.json and "
            "openapi/first-slice-v1.yaml: "
            f"missing from OpenAPI: {missing_from_openapi}; "
            f"missing from manifest: {missing_from_manifest}"
        )

    rows.sort(key=lambda item: (item["path"], item["method"]))
    return rows, len(manifest_keys), len(openapi_keys)


def render_api_index(rows: list[dict[str, Any]], permission_ids: list[str]) -> str:
    lines: list[str] = [
        GENERATED_HEADER,
        "// Source: registry/endpoint-permissions.json + openapi/first-slice-v1.yaml",
        "//",
        "// registry/architecture-rules.json restricts the \"contracts\" package family",
        "// to depend only on \"foundation\" (no intra-family dependency is allowed, per",
        "// docs/blueprint/14-Engineering/ARCHITECTURE_DEPENDENCY_RULES.md §Contracts).",
        "// packages/contracts/api therefore cannot import PermissionId from",
        "// @meridian/contracts-permissions; the permission id union is regenerated and",
        "// inlined here from the same source of truth (registry/permissions.json).",
        "",
        "export const KNOWN_PERMISSION_IDS = [",
    ]
    for permission_id in permission_ids:
        lines.append(f"\t{ts_string(permission_id)},")
    lines.append("] as const;")
    lines.append("")
    lines.append("export type PermissionId = (typeof KNOWN_PERMISSION_IDS)[number];")
    lines.append("")
    lines.append('export type Authorization = "authenticated_session" | "authenticated_membership";')
    lines.append("")
    method_union = " | ".join(ts_string(method) for method in HTTP_METHODS)
    lines.append("export interface EndpointDefinition {")
    lines.append("\tauthorization?: Authorization;")
    lines.append(f"\tmethod: {method_union};")
    lines.append("\toperationId: string;")
    lines.append("\tpath: string;")
    lines.append("\tpermission?: PermissionId;")
    lines.append("}")
    lines.append("")
    lines.append("export const ENDPOINTS = [")
    for row in rows:
        # Property order matches Biome's assist/source/useSortedKeys (alphabetical
        # object-literal keys), so the generator's output is already lint-clean.
        parts = []
        if "authorization" in row:
            parts.append(f"authorization: {ts_string(row['authorization'])}")
        parts.append(f"method: {ts_string(row['method'])}")
        parts.append(f"operationId: {ts_string(row['operationId'])}")
        parts.append(f"path: {ts_string(row['path'])}")
        if "permission" in row:
            parts.append(f"permission: {ts_string(row['permission'])}")
        lines.append(f"\t{{ {', '.join(parts)} }},")
    lines.append("] as const satisfies readonly EndpointDefinition[];")
    lines.append("")
    lines.append("export type Endpoint = (typeof ENDPOINTS)[number];")
    lines.append("")
    lines.append("export type EndpointsByPermission = {")
    lines.append("\t[Permission in PermissionId]: Extract<Endpoint, { permission: Permission }>;")
    lines.append("};")
    lines.append("")
    lines.append("export type EndpointsByOperationId = {")
    lines.append('\t[Id in Endpoint["operationId"]]: Extract<Endpoint, { operationId: Id }>;')
    lines.append("};")
    lines.append("")
    return "\n".join(lines) + "\n"


def render_platform_api_metadata(rows: list[dict[str, Any]]) -> str:
    lines = [
        GENERATED_HEADER,
        "// Source: openapi/first-slice-v1.yaml + registry/endpoint-permissions.json",
        "",
        "export const OPENAPI_OPERATION_METADATA = [",
    ]
    for row in rows:
        parts = []
        for key in (
            "authorization",
            "method",
            "operationId",
            "path",
            "permission",
            "requestRef",
            "responseRef",
            "successStatus",
        ):
            if key not in row:
                continue
            value = row[key]
            rendered = str(value) if isinstance(value, int) else ts_string(value)
            parts.append(f"{key}: {rendered}")
        lines.append(f"\t{{ {', '.join(parts)} }},")
    lines.extend(
        [
            "] as const;",
            "",
            "export type OpenApiOperationMetadata =",
            "\t(typeof OPENAPI_OPERATION_METADATA)[number];",
            "",
        ]
    )
    return "\n".join(lines)


