#!/usr/bin/env python3
"""Generate deterministic TypeScript contract packages from governed registries.

Mirrors the determinism, `--check` semantics, and stdlib-only constraints of
`scripts/generate_registries.py`: registries and the OpenAPI contract are the
single source of truth, and the generated TypeScript files under
`packages/contracts/*/src` are never hand-edited. The OpenAPI file is parsed
with the same line-oriented regex technique as
`scripts/validate_docs.py::collect_openapi_operations` so CI never grows a
YAML dependency.
"""

from __future__ import annotations

import argparse
import json
import re
import subprocess
import sys
import tempfile
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parents[1]

GENERATED_HEADER = "// GENERATED by scripts/generate_contracts.py — DO NOT EDIT."

IDENTIFIER = re.compile(r"^[A-Za-z_$][A-Za-z0-9_$]*$")

# Same extraction regexes as scripts/validate_docs.py::collect_openapi_operations,
# adapted to keep the original (non-normalized) OpenAPI path around for reuse.
OPENAPI_PATH = re.compile(r"^  (/[^:]+):\s*$")
OPENAPI_METHOD = re.compile(r"^    (get|post|put|patch|delete|options|head|trace):\s*$")
OPENAPI_OPERATION_ID = re.compile(r"^      operationId:\s*(\S+)\s*$")
OPENAPI_PERMISSION = re.compile(r"^      x-permission:\s*(\S+)\s*$")
OPENAPI_AUTHORIZATION = re.compile(r"^      x-authorization:\s*(\S+)\s*$")
OPENAPI_RESPONSE_STATUS = re.compile(r"^        ['\"]?(2\d\d)['\"]?:\s*$")
OPENAPI_ANY_RESPONSE = re.compile(r"^        (?:['\"]?\d{3}['\"]?|default):\s*$")
OPENAPI_REF = re.compile(r"\$ref:\s*['\"]([^'\"]+)['\"]")

HTTP_METHODS = ["GET", "POST", "PUT", "PATCH", "DELETE", "OPTIONS", "HEAD", "TRACE"]


def load_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def ts_string(value: str) -> str:
    return json.dumps(value)


def ts_key(key: str) -> str:
    return key if IDENTIFIER.fullmatch(key) else ts_string(key)


# ---------------------------------------------------------------------------
# Capabilities
# ---------------------------------------------------------------------------


def load_capability_ids() -> list[str]:
    data = load_json(ROOT / "registry" / "capabilities.json")
    return sorted(str(item["id"]) for item in data["capabilities"])


def render_capabilities_index(capability_ids: list[str]) -> str:
    lines: list[str] = [
        GENERATED_HEADER,
        "// Source: registry/capabilities.json",
        "",
        "export const CAPABILITY_IDS = [",
    ]
    for capability_id in capability_ids:
        lines.append(f"\t{ts_string(capability_id)},")
    lines.append("] as const;")
    lines.append("")
    lines.append("export type CapabilityId = (typeof CAPABILITY_IDS)[number];")
    lines.append("")
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Permissions
# ---------------------------------------------------------------------------


def load_permission_ids() -> list[str]:
    data = load_json(ROOT / "registry" / "permissions.json")
    return sorted(str(item["id"]) for item in data["permissions"])


def load_permissions_by_namespace() -> dict[str, list[str]]:
    data = load_json(ROOT / "registry" / "permissions.json")
    grouped: dict[str, list[str]] = {}
    for item in data["permissions"]:
        grouped.setdefault(str(item["namespace"]), []).append(str(item["id"]))
    for namespace in grouped:
        grouped[namespace].sort()
    return dict(sorted(grouped.items()))


def render_permissions_index(permission_ids: list[str], grouped: dict[str, list[str]]) -> str:
    lines: list[str] = [
        GENERATED_HEADER,
        "// Source: registry/permissions.json",
        "",
        "export const PERMISSION_IDS = [",
    ]
    for permission_id in permission_ids:
        lines.append(f"\t{ts_string(permission_id)},")
    lines.append("] as const;")
    lines.append("")
    lines.append("export type PermissionId = (typeof PERMISSION_IDS)[number];")
    lines.append("")
    lines.append("export const PERMISSIONS_BY_NAMESPACE = {")
    for namespace, ids in grouped.items():
        lines.append(f"\t{ts_key(namespace)}: [")
        for permission_id in ids:
            lines.append(f"\t\t{ts_string(permission_id)},")
        lines.append("\t],")
    lines.append("} as const satisfies Record<string, readonly PermissionId[]>;")
    lines.append("")
    return "\n".join(lines) + "\n"


